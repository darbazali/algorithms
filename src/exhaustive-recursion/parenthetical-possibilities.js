/* 
parenthetical possibilities
---------------------------
Write a function, parentheticalPossibilities, that takes in a string as an argument. 
The function should return an array containing all of the strings that could be generated by expanding all parentheses of the string into its possibilities.

For example, the possibilities for 'x(mn)yz' are 'xmyz', 'xnyz'.

@test-cases:
------------
parentheticalPossibilities('x(mn)yz'); // ->  [ 'xmyz', 'xnyz' ]
parentheticalPossibilities("(qr)ab(stu)c"); // -> [ 'qabsc', 'qabtc', 'qabuc', 'rabsc', 'rabtc', 'rabuc' ]

*/

const getChoices = (s = "") => {
  if (s[0] === "(") {
    const end = s.indexOf(")")
    const choices = s.slice(1, end)
    const remainder = s.slice(end + 1)
    return { choices, remainder }
  }

  return { choices: s[0], remainder: s.slice(1) }
}

const parentheticalPossibilities = (s = "") => {
  if (s.length === 0) return [""]
  const allPossibilities = []

  const { choices, remainder } = getChoices(s)
  for (let choice of choices) {
    const remainderPossibilities = parentheticalPossibilities(remainder)
    for (let substring of remainderPossibilities) {
      allPossibilities.push(choice + substring)
    }
  }

  return allPossibilities
}

console.log(parentheticalPossibilities("x(mn)yz"))
console.log(parentheticalPossibilities("(qr)ab(stu)c"))
